function clickAfter(who) {
    if(who==='个人中心'){
        window.location.href = "./login.html"
    }
    else if(who==='文创产品'){
        window.location.href = "./goods.html"
    }
    else if(who==='校园风景'){
        window.location.href = "./view.html"
    }
    else if(who==='预约管理'){
        window.location.href = "./book.html"
    }
    else if(who==='首页'){
        window.location.href = "./index.html"
    }
}